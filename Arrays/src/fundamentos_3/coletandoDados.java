package fundamentos_3;

public class coletandoDados {
	public static void main(String[] args) {
		double[] intArray = { 2, 5, 46, 12, 34 };

        System.out.print("Valores: ");
        for (int i = 0; i < intArray.length; i++) {
            System.out.print(intArray[i] + " ");
        }
	}
}

