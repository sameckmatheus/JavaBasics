package fundamentos;

public class variaveisIntroducao {
	public static void main(String[] args) {
		String sayHello = "Hello, World!";
		System.out.println(sayHello);
		
		int x = 10;
		
		System.out.println(x++);
		System.out.println(++x);
		System.out.println(x--);
		System.out.println(--x);
	}
}
